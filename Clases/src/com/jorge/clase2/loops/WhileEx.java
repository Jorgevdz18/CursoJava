public class WhileEx {
	
	//Metodo main. corre aplicacion
	public static void main(String[] args) {
		/*
		 * Bloque while ejecuta un bloque de codigo
		 * siempre que su condicion sea verdadera.
		 * 
		 * while(condition){
		 * 		codigo a ejecutar;
		 * 		---
		 * 		---
		 * }
		 */
		
		int i = 1;
		while(i <= 1000) {
			System.out.println(i);
			i++;
		}
	}

}
