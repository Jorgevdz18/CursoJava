public class ForEx {
	
	//Metodo main.corre la aplicacion
	
	public static void main(String[] args) {
		/*
		 * For loop
		 * 
		 * for(es una variable de integracion; condicion; incremento){
		 * 	-------
		 * 	-------
		 */
		for(int i = 0; i <= 100; i++){
			System.out.println("8 x" + i + " = " + (8 * i) );
		}
	}

}
